    Expression_DistObjects:function()
    {
        var p1 = this.ho.getExpParam();
        var p2 = this.ho.getExpParam();

        var object1, object2;
        if (p1 == 0)
        {
            object1 = this.getCurrentObject(null);
        }
        else
        {
            object1 = this.ho.getObjectFromFixed(p1);
        }

        if (p2 == 0)
        {
            object2 = this.getCurrentObject(null);
        }
        else
        {
            object2 = this.ho.getObjectFromFixed(p2);
        }

        if (object1 == null || object2 == null)
        {
            return -1;
        }

        var fp1 = object1.hoX;
        var fp2 = object1.hoY;
        var fp3 = object2.hoX;
        var fp4 = object2.hoY;
        return Math.sqrt((fp1 - fp3) * (fp1 - fp3) + (fp2 - fp4) * (fp2 - fp4));
    },

