    Action_SET_Projectile_RotateTowardsAngle:function(act)
    {
        var object = this.getCurrentObject(CRunclickteam_movement_controller.DLL_VECTOR);
        if (object != null)
        {
            var param1 = act.getParamExpression(this.rh, 0);
            var param2 = act.getParamExpression(this.rh, 1);
            var newangleTow = (param1 % 360);	// angle towards
            var newangleAdd = (param2 % 360);	// angle to add

            var currentAngle = this.ho.callMovement(object, CRunclickteam_movement_controller.GET_Projectile_Dir, 0);

            var difM = currentAngle - newangleTow;
            if (difM < 0)
            {
                difM += 360;
            }

            var difA = 360 - difM;

            if (difM <= difA)
            {
                if (difM < newangleAdd)
                {
                    currentAngle -= difM;
                }
                else
                {
                    currentAngle -= newangleAdd;
                }
            }
            else
            {
                if (difA < newangleAdd)
                {
                    currentAngle += difA;
                }
                else
                {
                    currentAngle += newangleAdd;
                }
            }
            this.ho.callMovement(object, CRunclickteam_movement_controller.SET_Projectile_Dir, currentAngle);
        }
    },

